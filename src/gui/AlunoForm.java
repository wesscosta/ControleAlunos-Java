    private void cadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarButtonActionPerformed
        aluno.setMatricula(Integer.valueOf(matriculaField.getText()));
        aluno.setNome(nomeField.getText());
        aluno.setEmail(emailField.getText());
        aluno.setCpf(cpfField.getText());
        Endereco end = aluno.getEndereco();
        end.setTipoLogradouro(logradouroCombo.getSelectedItem().toString());
        end.setNomeLogradouro(logradouroField.getText());
        end.setBairro(bairroField.getText());
        end.setCep(cepField.getText());
        end.setComplemento(complementoField.getText());
        end.setNumero(Integer.valueOf(numeroField.getText()));
        end.setEstado(estadoField.getText());
        end.setCidade(cidadeField.getText());
        end.setTelefon(telefoneField.getText());
        
        if(masculinoRadion.isSelected())
            aluno.setSexo('M');
        else 
            aluno.setSexo('F');
        this.setVisible(false);
    }//GEN-LAST:event_cadastrarButtonActionPerformed

