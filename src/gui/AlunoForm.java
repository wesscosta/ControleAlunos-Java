    private void setFields(){
        matriculaField.setText(String.valueOf(aluno.getMatricula()));
        nomeField.setText(aluno.getNome());
        emailField.setText(aluno.getEmail());
        cpfField.setText(aluno.getCpf());
        if(aluno.getSexo()=='M'){
            masculinoRadion.setSelected(true);
        }else{
            femininoRadio.setSelected(true);
        }
        Endereco end = aluno.getEndereco();
        logradouroCombo.setSelectedItem(end.getTipoLogradouro());
        logradouroField.setText(end.getNomeLogradouro());
        numeroField.setText(String.valueOf(end.getNumero()));
        complementoField.setText(end.getComplemento());
        bairroField.setText(end.getBairro());
        cepField.setText(end.getCep());
        cidadeField.setText(end.getCidade());
        estadoField.setText(end.getEstado());
        telefoneField.setText(end.getTelefon());
        
        
    }
