    private void updateTable(){
        DefaultTableModel tableModel=  (DefaultTableModel) alunosTable.getModel();
        tableModel.setNumRows(0);
        alunos = bd.selectAll();
        for(Aluno a : alunos ){
            tableModel.addRow(new String[]{a.getNome(),String.valueOf(a.getMatricula())});
            System.out.println("add");
        }
        alunosTable.setModel(tableModel);
    }
    
