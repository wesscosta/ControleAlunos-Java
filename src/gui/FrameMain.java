    private void adicionarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarButtonActionPerformed
        this.aluno = new Aluno();
        
        AlunoForm alunoForm = new AlunoForm(this, aluno);
        alunoForm.setVisible(true);
        alunoForm.dispose();
        if (aluno.getMatricula() != 0) {
            //alunos.add(aluno);
            bd.insertAluno(aluno);
        }
            
        updateTable();
    }//GEN-LAST:event_adicionarButtonActionPerformed

    private void tableSelected(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSelected

        if(evt.getButton()==3 && (alunosTable.getSelectedRow()!=-1)){
            menuPopup.show(alunosTable, evt.getX(), evt.getY());
        }
                
    }//GEN-LAST:event_tableSelected

    private void exibirMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exibirMenuActionPerformed
        int n = alunosTable.getSelectedRow();
        aluno = alunos.get(n);
        AlunoForm alunoForm = new AlunoForm(this, aluno);
        alunoForm.setEditable(false);
        alunoForm.setVisible(true);
    }//GEN-LAST:event_exibirMenuActionPerformed

    private void alterarMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarMenuActionPerformed
        int n = alunosTable.getSelectedRow();
        aluno = alunos.get(n);
        AlunoForm alunoForm = new AlunoForm(this, aluno);
        alunoForm.setVisible(true);
        alunos.set(n, aluno);
        updateTable();
    }//GEN-LAST:event_alterarMenuActionPerformed

    private void removerMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerMenuActionPerformed
        int n = alunosTable.getSelectedRow();
        alunos.remove(n);
        updateTable();
        
        
    }//GEN-LAST:event_removerMenuActionPerformed

    private void exportarMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportarMenuActionPerformed
        String s = JOptionPane.showInputDialog("Digite o nome do arquivo");
        try{
            AlunoDAO alunoDAO = new AlunoDAO(s);
            for (Aluno aluno : alunos) {
                alunoDAO.insert(aluno);
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"Arquivo não criado");
        }
            
        
    }//GEN-LAST:event_exportarMenuActionPerformed

    private void importarMenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importarMenuActionPerformed
        try {
            String n = JOptionPane.showInputDialog("Digite o nome do arquivo de importação");
            alunos = AlunoDAO.selectAll(n);
        } catch (IOException ex) {
            Logger.getLogger(FrameMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        updateTable();        
    }//GEN-LAST:event_importarMenuActionPerformed

    
